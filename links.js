let my_works_div = document.getElementById("my-works-div-1");
categories.forEach(category => {
    let container = document.createElement("div");
    container.classList.add("div-container");
    let h2 = document.createElement("h2");
    h2.textContent = `${category[0]} (${category[1].length})`;
    container.appendChild(h2);
    let div = document.createElement("div");
    div.classList.add("link-list");
    category[1].forEach(item => {
        let a = document.createElement("a");
        a.classList.add(`link-item-${categories.indexOf(category)}-${category[1].indexOf(item)}`);
        a.addEventListener("click", function (){
            showDescription(`link-item-${categories.indexOf(category)}-${category[1].indexOf(item)}`);
        });
        let title_container = document.createElement("div");
        title_container.classList.add("title-container");
        let title = document.createElement("p");
        title.classList.add("title");
        title.style.animation = "unset";
        title.textContent = item[1];
        title_container.appendChild(title);
        a.appendChild(title_container);
        let image_container = document.createElement("div");
        image_container.classList.add("image-container");
        if (item[3]){
                item[3].forEach(image => {
                let inside_image_container = document.createElement("div");
                let project_image = document.createElement("img");
                project_image.setAttribute("data-lazy", image);
                inside_image_container.appendChild(project_image);
                image_container.appendChild(inside_image_container);
            });
        }
        a.appendChild(image_container);
        let description_header = document.createElement("p");
        description_header.classList.add("description-header");
        description_header.textContent = "Description";
        a.appendChild(description_header);
        let description = document.createElement("p");
        description.classList.add("description");
        description.textContent = item[2] ? item[2] : "No description available.";
        a.appendChild(description);
        let href = document.createElement("a");
        href.classList.add("visit-link");
        href.href = item[0];
        href.textContent = "Visit link";
        a.appendChild(href);
        image_container.style.display = "none";
        image_container.addEventListener("wheel", event => {
            event.preventDefault();
            const delta = Math.sign(event.deltaY);
            image_container.scrollLeft += delta * 50;
        });
        description_header.style.display = "none";
        description.style.display = "none";
        href.style.display = "none";
        div.appendChild(a);
    });
    container.appendChild(div);
    my_works_div.appendChild(container);
    if (categories.indexOf(category) === categories.length - 1){
        return;
    }
    let spacer = document.createElement("div");
    let separator = document.createElement("div");
    spacer.classList.add("spacer");
    separator.classList.add("separator");
    my_works_div.appendChild(spacer);
    my_works_div.appendChild(separator);
    my_works_div.appendChild(spacer.cloneNode());
});

let current_description_index = null;

function showDescription(index){
    let titles = document.getElementsByClassName("title");
    let image_containers = document.getElementsByClassName("image-container");
    let descriptions = document.getElementsByClassName("description");
    let description_headers = document.getElementsByClassName("description-header");
    let visit_links = document.getElementsByClassName("visit-link");
    for (let i = 0; i < descriptions.length; i++){
        if (titles[i].style.animation != "unset"){
            titles[i].style.animation = "title-unscale 0.3s ease-in-out forwards";
        }
        image_containers[i].style.display = "none";
        descriptions[i].style.display = "none";
        description_headers[i].style.display = "none";
        visit_links[i].style.display = "none";
    }
    if (current_description_index === index){
        document.getElementsByClassName(index)[0].getElementsByClassName("title")[0].style.animation = "title-unscale 0.3s ease-in-out forwards";
        setTimeout(() => {
            document.getElementsByClassName(index)[0].getElementsByClassName("title")[0].style.animation = "unset";
        }, 300);
        current_description_index = null;
    } else {
        current_description_index = index;
        let selected_image_container = document.getElementsByClassName(index)[0].getElementsByClassName("image-container")[0];
        let selected_description = document.getElementsByClassName(index)[0].getElementsByClassName("description")[0];
        let selected_description_header = document.getElementsByClassName(index)[0].getElementsByClassName("description-header")[0];
        let selected_visit_link = document.getElementsByClassName(index)[0].getElementsByClassName("visit-link")[0];
        selected_image_container.style.display = "flex";
        selected_description.style.display = "block";
        selected_description_header.style.display = "block";
        document.getElementsByClassName(index)[0].getElementsByClassName("title")[0].style.animation = "title-scale 0.3s ease-in-out forwards";
        selected_visit_link.style.display = "block";
    }
}